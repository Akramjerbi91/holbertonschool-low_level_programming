/**
 * op_add - adds 2 ints
 * @a: int
 * @b: int
 *
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - substruts a from b
 * @a: int
 * @b: int
 *
 * Return: d difference
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplies a by b
 * @a: int
 * @b: int
 *
 * Return: p product
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divide a by b
 * @a: int
 * @b: int
 *
 * Return: r result of div
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - a modulus b
 * @a: int
 * @b: int
 *
 * Return: m result of modulus
 */
int op_mod(int a, int b)
{
	return (a % b);
}
